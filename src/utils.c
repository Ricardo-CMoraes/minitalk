#include "minitalk.h"

int	convert_to_integer(char	*b)
{
	int	i;
	int	j;
	int	nb;

	i = 0;
	j = 128;
	nb = 0;
	while (i < 8)
	{
		nb = j * b[i] + nb;
		i++;
		j = j / 2;
	}
	return (nb);
}
